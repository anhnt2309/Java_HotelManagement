    public void LoadDataToTable() {
        dsDatPhong = new ArrayList<DatPhong>();
        dsDatPhong = DatPhong_Controller.getDSDP();
        DefaultTableModel model = (DefaultTableModel) Table_DatPhong.getModel();
        //code gọi
        DatPhong_Controller dp = new DatPhong_Controller();
        String madp = dp.LayMaDP();
        ////set vào textbox
        Text_MADP.setText(madp);
        Object[] row = new Object[7];
        for (int i = 0; i < dsDatPhong.size(); i++) {
            row[0] = dsDatPhong.get(i).getMaDK();
            row[1] = dsDatPhong.get(i).getMaNV();
            row[2] = dsDatPhong.get(i).getMaPhong();
            row[3] = dsDatPhong.get(i).getMaKH();

            row[4] = dateFormat(dsDatPhong.get(i).getNgayDK().toString());
            row[5] = dateFormat(dsDatPhong.get(i).getNgayDen().toString());
            row[6] = dsDatPhong.get(i).getSoNguoi();
            model.addRow(row);
        }

        ArrayList<Phong> dsPhong = new ArrayList<Phong>();
        dsPhong = Phong_Controller.getDSPhong();

        for (int j = 0; j < dsPhong.size(); j++) {
            PhongDat_DP.addItem(dsPhong.get(j).getMaPhong());
        }

        ArrayList<Tblnhanvien> dsNV = new ArrayList<Tblnhanvien>();
        dsNV = NhanVien_Controller.getDSNV();

        for (int j = 0; j < dsNV.size(); j++) {
            Combo_MaNV.addItem(dsNV.get(j).getManv());
        }

        dsKH = new ArrayList<KhachHang>();
        dsKH = KhachHang_Controller.getDSKH();

        for (int j = 0; j < dsKH.size(); j++) {
            Text_MaKH.addItem(dsKH.get(j).getMaKH());
        }

    }

